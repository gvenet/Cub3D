#include "../../libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *m;

	m = (char*)s;
	m--;
	while (*++m || (c == 0 && *m == 0))
		if (*m == (char)c)
			return (m);
	return (NULL);
}
